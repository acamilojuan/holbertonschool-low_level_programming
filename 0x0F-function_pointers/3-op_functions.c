/**
 * op_add - function sum
 * op_sub - function sub
 * op_mul - function mult
 * op_div - function division
 * op_mod - function modulation
 * @a: varibale a
 * @b: variable b
 */
int op_add(int a, int b)
{
	return (a + b);
} 	
int op_sub(int a, int b)
{
	return (a - b);
}
int op_mul(int a, int b)
{
	return (a * b);
}
int op_div(int a, int b)
{
	return (a / b);
}
int op_mod(int a, int b)
{
	return (a % b);
}
